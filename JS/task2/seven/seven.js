
let drink = prompt("Enter your drink:");
switch (drink) {
    case "tea":
        alert("The price of tea is 2.75 dollars");
        break;

    case "juice":
        alert("The price of juice is 3.75 dollars");
        break;

    case "coffee":
        alert("The price of coffee is 4.75 dollars");
        break;

    case "matcha":
        alert("The price of matcha is 5.75 dollars");
        break;
    default:
        alert("Not in the menu")
}
